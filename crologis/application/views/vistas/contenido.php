        <h2>ASESORÍA PARA LA ELEVACIÓN DE LA CALIDAD DE LOS PRODUCTOS/SERVICIOS</h2>
        <img class="featurette-image  img-responsive pull-left thumbnail" src="<?=base_url()?>asset/imagenes/ser.jpg" width="500px" height="500px">
        <p class="espacio">
          Las empresas requieren escalar sus niveles de competitividad, las presiones de un mundo globalizado, requieren que las empresas estén aptas para presentarse en cualquier escenario y ser una opción real para los clientes.

<br>
Para lograrlo las empresas deben trazarse estrategias de competitividad, siendo la calidad una de las más relevantes.
<br>
Mediante una estrategia de calidad una empresa puede:
<br>
Identificar su mercado meta y las necesidades actuales y potenciales
Determinar las características de sus productos que satisfacen la necesidad de su mercado
Diseñar o rediseñar sus procesos de producción y administrativos
Identificar costos de calidad y de no calidad y posicionarlos dentro de sus estados financieros
Cumplir con acuerdos contractuales o disposiciones regulatorias
Lograr certificaciones y acreditaciones
<br>

Los servicios que ofrecemos para la elevación de la calidad son:
<br>
<ul>
    <li>1- Reingeniería y Optimización en Procesos</li>
    <li>2- Mapeo y documentación de procesos y procedimientos</li>
    <li>3- Implementación de Sistemas de Gestión (Calidad, Seguridad, Ambiental)</li>
    <li>4- Asesoría para la certificación y mantenimiento de Sistemas de Gestión (Calidad, Seguridad, Ambiental)</li>
    <li>5- Asesoría para la mejora continua de procesos</li>
    <li>6- Realización de Auditorías Internas ISO 9001: 2008</li>
</ul>
<br>
Podemos realizar las auditorías internas al Sistema de Gestión de la Calidad ISO 9001: 2008, este aplica tanto como para clientes certificados en dicha norma como para clientes que esperan certificarse o simplemente obtener una opinión de segunda parte acerca de la eficacia de su Sistema de Gestión de la Calidad. También podemos realizar esta auditoría a nombre de clientes que deseen evaluar el Sistema de Calidad de sus Proveedores. Las Auditorias Internas las llevamos a cabo en base a los lineamientos expresados en la norma ISO 19011: 2011, la cual define las directrices para realizar auditorias a los Sistemas de Gestión.   


